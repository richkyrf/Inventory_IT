    void loadData() {
        DRunSelctOne dRunSelctOne = new DRunSelctOne();
        dRunSelctOne.seterorm("Gagal Menampilkan Data Barang Rusak");
        dRunSelctOne.setQuery("SELECT `IdBarangRusak`, `JenisBarang`, `KategoriBarang`, `NamaBarang`, `NamaKaryawan`, `TanggalBeli`, `TanggalRusak`, A.`Keterangan` FROM `tbbarangrusak` AS A JOIN `tbmbarang` AS B ON A.`IdBarang`=B.`IdBarang` JOIN `tbmkategoribarang` AS C ON B.`IdKategoriBarang`=C.`IdKategoriBarang` JOIN `tbmjenisbarang` AS D ON C.`IdJenisBarang`=D.`IdJenisBarang` JOIN `tbmkaryawan` AS E ON A.`IdKaryawan`=E.`IdKaryawan` WHERE `IdBarangRusak`='" + idEdit + "'");
        ArrayList<String> list = dRunSelctOne.excute();
        JCJenisBarang.setSelectedItem(list.get(1));
        JCKategoriBarang.setSelectedItem(list.get(2));
        JCNamaBarang.setSelectedItem(list.get(3));
        JCNamaPemakai.setSelectedItem(list.get(4));
        JDTanggalBeli.setDate(FDateF.strtodate(list.get(5), "yyyy-MM-dd"));
        JDTanggalRusak.setDate(FDateF.strtodate(list.get(6), "yyyy-MM-dd"));
        JTKeterangan.setText(list.get(7));
    }

