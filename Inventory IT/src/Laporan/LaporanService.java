    boolean checkInput() {
        if (JDTanggalServiceAwal.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Tanggal Service Awal Tidak Boleh Kosong", "Information", JOptionPane.INFORMATION_MESSAGE);
            JDTanggalServiceAwal.requestFocus();
            return false;
        } else if (JDTanggalServiceAkhir.getDate() == null) {
            JOptionPane.showMessageDialog(this, "Tanggal Service Akhir Tidak Boleh Kosong", "Information", JOptionPane.INFORMATION_MESSAGE);
            JDTanggalServiceAkhir.requestFocus();
            return false;
        } else if (JDTanggalServiceAkhir.getDate().compareTo(JDTanggalServiceAwal.getDate()) < 0) {
            JOptionPane.showMessageDialog(this, "Tanggal Service Akhir Tidak Boleh Lebih Kecil Dari Tanggal Service Awal", "Information", JOptionPane.INFORMATION_MESSAGE);
            JDTanggalServiceAkhir.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    void tampilkan() {
        if (checkInput()) {
            String jenisBarangService, namaBarangService, jenisService;
            HashMap hashs = new HashMap();
            FLaporan fLaporan = new FLaporan();
            hashs.put("Title", "Laporan Service");
            hashs.put("TanggalServiceAwal", FDateF.datetostr(JDTanggalServiceAwal.getDate(), "dd/MM/yyyy"));
            hashs.put("TanggalServiceAkhir", FDateF.datetostr(JDTanggalServiceAkhir.getDate(), "dd/MM/yyyy"));
            if (JCJenisService.getSelectedIndex() == 0) {
                jenisService = "";
                hashs.put("JenisService", "Semua Jenis");
            } else {
                jenisService = " AND `JenisService`='" + JCJenisService.getSelectedItem() + "'";
                hashs.put("JenisService", JCJenisService.getSelectedItem());
            }
            if (JCJenisBarangService.getSelectedIndex() == 0) {
                jenisBarangService = "";
                hashs.put("JenisBarang", "Semua Jenis");
            } else {
                jenisBarangService = " AND `JenisBarangService`='" + JCJenisBarangService.getSelectedItem() + "'";
                hashs.put("JenisBarang", JCJenisBarangService.getSelectedItem());
            }
            if (JCNamaBarangService.getSelectedIndex() == 0) {
                namaBarangService = "";
                hashs.put("NamaBarang", "Semua Barang");
            } else {
                namaBarangService = " AND `NamaBarangService`='" + JCNamaBarangService.getSelectedItem() + "'";
                hashs.put("NamaBarang", JCNamaBarangService.getSelectedItem());
            }
            hashs.put("PrintedBy", "Di Print Oleh " + GlobalVar.VarL.username + " Pada " + FDateF.datetostr(new Date(), "dd/MM/yyyy HH:mm"));
            hashs.put("Where", " AND `TanggalService` BETWEEN '" + FDateF.datetostr(JDTanggalServiceAwal.getDate(), "yyyy-MM-dd") + "' AND '" + FDateF.datetostr(JDTanggalServiceAkhir.getDate(), "yyyy-MM-dd") + "'" + jenisService + jenisBarangService + namaBarangService);
            fLaporan.sethashmap(hashs);
            fLaporan.setfilename("LaporanService");
            fLaporan.setErorm("Gagal Menampilkan " + this.getTitle());
            fLaporan.excute();
            History.simpanhistory(GlobalVar.VarL.username, "Melihat Laporan Service");
        }
    }

