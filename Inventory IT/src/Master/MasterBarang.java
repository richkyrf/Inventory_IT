    boolean checkInput() {
        if (JCJenisBarang.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Jenis Barang Harus Dipilih", "Information", JOptionPane.INFORMATION_MESSAGE);
            JCJenisBarang.requestFocus();
            return false;
        } else if (JCKategoriBarang.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Kategori Barang Harus Dipilih", "Information", JOptionPane.INFORMATION_MESSAGE);
            JCKategoriBarang.requestFocus();
            return false;
        } else if (JTNamaBarang.getText().replace(" ", "").isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama Barang Tidak Boleh Kosong", "Information", JOptionPane.INFORMATION_MESSAGE);
            JTNamaBarang.requestFocus();
            return false;
        } else if (!JRBGaransiYa.isSelected() && !JRBGaransiTidak.isSelected()) {
            JOptionPane.showMessageDialog(this, "Garansi Harus Dipilih", "Information", JOptionPane.INFORMATION_MESSAGE);
            JRBGaransiYa.requestFocus();
            return false;
        } else if (JTSatuan.getText().replace(" ", "").isEmpty()) {
            JOptionPane.showMessageDialog(this, "Satuan Tidak Boleh Kosong", "Information", JOptionPane.INFORMATION_MESSAGE);
            JTSatuan.requestFocus();
            return false;
        } else {
            return true;
        }
    }

    void tambah(boolean tutup) {
        if (checkInput()) {
            String garansi;
            if (JRBGaransiYa.isSelected()) {
                garansi = "Ya";
            } else {
                garansi = "Tidak";
            }
            Insert insert = new LSubProces.Insert();
            boolean simpan = insert.Simpan("INSERT INTO `tbmbarang`(`IdKategoriBarang`, `NamaBarang`, `Garansi`, `Satuan`, `Keterangan`) VALUES ((SELECT `IdKategoriBarang` FROM `tbmkategoribarang` AS A JOIN `tbmjenisbarang` AS B ON A.`IdJenisBarang`=B.`IdJenisBarang` WHERE `JenisBarang`='" + JCJenisBarang.getSelectedItem() + "' AND `KategoriBarang`='" + JCKategoriBarang.getSelectedItem() + "'), '" + JTNamaBarang.getText() + "', '" + garansi + "', '" + JTSatuan.getText() + "', '" + JTKeterangan.getText() + "')", "Tambah Data Master Barang", this);
            if (simpan) {
                if (GlobalVar.Var.listMasterBarang != null) {
                    GlobalVar.Var.listMasterBarang.load();
                }
                if (GlobalVar.Var.tambahBarangRusak != null) {
                    GlobalVar.Var.tambahBarangRusak.JCNamaBarang.load("SELECT '-- Pilih Nama Barang --' AS `NamaBarang` UNION SELECT `NamaBarang` FROM `tbmbarang` AS A JOIN `tbmkategoribarang` AS B ON A.`IdKategoriBarang`=B.`IdKategoriBarang` JOIN `tbmjenisbarang` AS C ON B.`IdJenisBarang`=C.`IdJenisBarang` WHERE `JenisBarang`='" + JCJenisBarang.getSelectedItem() + "' AND `KategoriBarang`='" + JCKategoriBarang.getSelectedItem() + "'");
                    GlobalVar.Var.tambahBarangRusak.JCNamaBarang.setSelectedItem(JTNamaBarang.getText());
                    GlobalVar.Var.tambahBarangRusak.JCNamaBarang.requestFocus();
                }
                if (GlobalVar.Var.editBarangRusak != null) {
                    GlobalVar.Var.editBarangRusak.JCNamaBarang.load("SELECT '-- Pilih Nama Barang --' AS `NamaBarang` UNION SELECT `NamaBarang` FROM `tbmbarang` AS A JOIN `tbmkategoribarang` AS B ON A.`IdKategoriBarang`=B.`IdKategoriBarang` JOIN `tbmjenisbarang` AS C ON B.`IdJenisBarang`=C.`IdJenisBarang` WHERE `JenisBarang`='" + JCJenisBarang.getSelectedItem() + "' AND `KategoriBarang`='" + JCKategoriBarang.getSelectedItem() + "'");
                    GlobalVar.Var.editBarangRusak.JCNamaBarang.setSelectedItem(JTNamaBarang.getText());
                    GlobalVar.Var.editBarangRusak.JCNamaBarang.requestFocus();
                }
                if (tutup) {
                    GlobalVar.Var.tambahMasterBarang.dispose();
                } else {
                    clearText();
                    JCJenisBarang.requestFocus();
                }
            }
        }
    }

