    public MasterBarang(Object id) {
        initComponents();
        setLocationRelativeTo(null);
        setDefaultCloseOperation(DISPOSE_ON_CLOSE);
        idEdit = id.toString();
        data = idEdit.split("\\+");
        if (idEdit.equals("0") || data.length > 1) {
            setTitle("Tambah Master Barang");
            if (data.length > 1) {
                JCJenisBarang.setSelectedItem(data[1]);
                JCKategoriBarang.setSelectedItem(data[2]);
                JTNamaBarang.requestFocus();
            }
            JBUbah.setVisible(false);
        } else {
            setTitle("Ubah Master Barang");
            loadData();
            JBTambah.setVisible(false);
            JBTambahTutup.setVisible(false);
        }
        setVisible(true);
    }

